huffman_coding_map = {
    "01": "0",
    "10": "4",
    "111": "D",
    "1100": "F",
    "1101": "E",
    "0001": "2",
    "0010": "1",
    "00001": "A",
    "00110": "3",
    "001110": "B",
    "001111": "C",
    "000000": "5",
    "00000100": "6",
    "00000101": "7",
    "00000110": "8",
    "00000111": "9",
}

cipher = "100000011100010110001111000001000000011100000100001110000001000000000001010000001000000101000100000100000000000001000000000000010100110000111010001011000000100000001000011110000010011000000010100000101000001000000000000010100010000010100110000101000001011000000100110000000100110000011101"

text = ""

bit_word = ""
for bit in cipher:
    bit_word += bit
